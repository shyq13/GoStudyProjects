package main

import "fmt"

func main() {
	// 1. +
	// I. 正数
	var p1 = +8
	fmt.Println("p1 = ", p1)
	// II. 相加
	var p2 = 1
	var p3 = 2
	fmt.Println("p2 + p3 = ", (p2 + p3))
	// III. 字符串拼接
	var p4 = "Hello "
	var p5 = "World"
	fmt.Println("p4 + p5 = ", (p4 + p5))

	fmt.Println("-----------------------------------------------------")

	// 2. /
}
